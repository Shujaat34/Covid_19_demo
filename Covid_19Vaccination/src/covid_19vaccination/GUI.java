    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtName.getText().isEmpty() || txtAge.getText().isEmpty() || txtPriority.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill the fields", "Message",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {

            String name = txtName.getText().toString();
            String age = txtAge.getText().toString();
            String priority = txtPriority.getText().toString();

            if (!Pattern.matches("[0-9]{1,4}", age)) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Age", "Message",
                        JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (!Pattern.matches("[0-9]{1,4}", priority)) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Priority", "Message",
                        JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            if (Integer.parseInt(priority) < 1 || Integer.parseInt(priority) > 10) {
                JOptionPane.showMessageDialog(this, "Please Enter Priority Between 1-10 (inclusive)", "Message",
                        JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            String data[] = {name, age, priority};

            patientList.add(new Patient(name, Integer.parseInt(age), Integer.parseInt(priority)));

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(data);
            
                   
            if(model.getRowCount() <=0){
                jButton2.setEnabled(false);
            }else{
                jButton2.setEnabled(true);
            }
            
            txtName.setText("");
            txtAge.setText(""); 
            txtPriority.setText("");
             

        }
    }//GEN-LAST:event_jButton1ActionPerformed

