    class ProgressThread extends Thread {

        JProgressBar pbar;

        ProgressThread(JProgressBar pbar) {
            this.pbar = pbar;
        }

        @Override
        public void run() {

            pbar.setMaximum(0);
            pbar.setMaximum(100);

            pbar.setValue(0);

            for (int i = 0; i < 100; i++) {

                pbar.setValue(i);
                try {
                    Thread.sleep(100);
                } catch (InterruptedException ex) {
                    Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

            if (pbar.getValue() == 99) {
                lblProgress.setText("All Patients Vaccinated...");
                DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
                while (dm.getRowCount() > 0) {
                    dm.removeRow(0);
                }

                jTable1.removeAll();

                Collections.sort(patientList, new Comparator<Patient>() {
                    @Override
                    public int compare(Patient o1, Patient o2) {
                        return o2.getPriority() - o1.getPriority();
                    }
                });
                
                dm = (DefaultTableModel) jTable2.getModel();
                for (Patient patient : patientList) {
                    String data[] = {patient.getName(),String.valueOf(patient.getAge()),String.valueOf(patient.getPriority())};
                    dm.addRow(data);
                }
                patientList.clear();
            }
        }
    }

